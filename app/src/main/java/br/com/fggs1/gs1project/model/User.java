package br.com.fggs1.gs1project.model;

public class User {

    private String name;
    private String email;
    private String password;

    public User() {
        // Empty constructor required.
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }
}
